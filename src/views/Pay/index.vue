<script setup>
import { getLatestOrderAPI } from '@/apis/order'
import { alipayAPI } from '@/apis/pay'
import { onMounted, ref } from 'vue'
import { useCountDown } from '@/composables/countDown';
// import { useRoute } from 'vue-router'

const { formatTime, start } = useCountDown()
// 获取订单数据
// const route = useRoute()
const payList = ref({})
const getPayInfo = async () => {
  const res = await getLatestOrderAPI()
  // console.log(res)
  payList.value = res.data.result
  // start(-1)
  start(payList.value.latestOrder.countdown)

}
onMounted(() => getPayInfo())


// 跳转支付
const paymentUrl = ref(null)
const goPay = async () => {
  try {
    const res = await alipayAPI({ orderId: payList.value.latestOrder.id, totalAmount: payList.value.latestOrder.totalAmount })
    paymentUrl.value = res.data.data.paymentUrl
    if (paymentUrl.value) {
      window.location.href = paymentUrl.value;
    }
  } catch (error) {
    console.error('Error initiating payment:', error)
  }
}


</script>


<template>
  <div class="pay-page">
    <div class="container">
      <!-- 付款信息 -->
      <div class="pay-info">
        <span class="icon iconfont icon-queren2"></span>
        <div class="tip">
          <p>订单提交成功！请尽快完成支付。</p>
          <p>支付还剩 <span>{{ formatTime }}</span>, 超时后将取消订单</p>
        </div>
        <div class="amount">
          <span>应付总额：</span>
          <span>¥{{ payList.latestOrder?.totalAmount.toFixed(2) }}</span>
        </div>
      </div>
      <!-- 付款方式 -->
      <div class="pay-type">
        <p class="head">选择以下支付方式付款</p>
        <div class="item">
          <p>支付平台</p>
          <a class="btn wx" href="javascript:;"></a>
          <!-- <a class="btn alipay" :href="payUrl"></a> -->
          <a class="btn alipay" @click="goPay"></a>
        </div>
        <div class="item">
          <p>支付方式</p>
          <a class="btn" href="javascript:;">招商银行</a>
          <a class="btn" href="javascript:;">工商银行</a>
